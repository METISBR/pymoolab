def _rwmop_ybus(linedata: np.ndarray, fscale: float) -> np.ndarray:
    linedata = np.array(linedata, dtype=float, copy=True)
    linedata[:, 3] = linedata[:, 3] * fscale
    fb = linedata[:, 0].astype(int) - 1
    tb = linedata[:, 1].astype(int) - 1
    r = linedata[:, 2]
    x = linedata[:, 3]
    b = linedata[:, 4]
    tap = linedata[:, 5]
    z = r + 1j * x
    y = 1.0 / z
    b = 1j * b
    nb = int(max(np.max(fb), np.max(tb))) + 1
    nl = len(fb)
    Y = np.zeros((nb, nb), dtype=complex)
    for k in range(nl):
        Y[fb[k], tb[k]] = Y[fb[k], tb[k]] - y[k] / tap[k]
        Y[tb[k], fb[k]] = Y[fb[k], tb[k]]
    for m in range(nb):
        for n in range(nl):
            if fb[n] == m:
                Y[m, m] = Y[m, m] + y[n] / (tap[n] ** 2) + b[n]
            elif tb[n] == m:
                Y[m, m] = Y[m, m] + y[n] + b[n]
    return Y


